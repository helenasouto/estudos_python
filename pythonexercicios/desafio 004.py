n = input('Digite algo: ')
print(n.isspace())
# se só tem espaço
print(n.isnumeric())
# se só tem número
print(n.isalpha())
# se só tem letra
print(n.isalnum())
# se tem letra e número
print(n.isupper())
# se aó tem letra maiúscula
print(n.islower())
# se só tem letra minúscula
print(n.istitle())






